CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER AS $$
BEGIN
  INSERT INTO public.profiles (id, email, name)
  VALUES (NEW.id, NEW.email, COALESCE(NEW.raw_user_meta_data->>'name', split_part(NEW.email, '@', 1)))
  ON CONFLICT (id) DO NOTHING;
  INSERT INTO public.credits (user_id, balance)
  VALUES (NEW.id, 100)
  ON CONFLICT (user_id) DO NOTHING;
  INSERT INTO public.user_settings (user_id, use_default_api)
  VALUES (NEW.id, TRUE)
  ON CONFLICT (user_id) DO NOTHING;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

CREATE OR REPLACE FUNCTION public.update_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER update_profiles_updated_at BEFORE UPDATE ON public.profiles
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at();
CREATE TRIGGER update_credits_updated_at BEFORE UPDATE ON public.credits
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at();
CREATE TRIGGER update_chat_history_updated_at BEFORE UPDATE ON public.chat_history
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at();
CREATE TRIGGER update_user_settings_updated_at BEFORE UPDATE ON public.user_settings
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at();

CREATE OR REPLACE FUNCTION public.get_credits_balance(p_user_id UUID)
RETURNS INTEGER AS $$
DECLARE v_balance INTEGER;
BEGIN
  SELECT balance INTO v_balance FROM public.credits WHERE user_id = p_user_id;
  RETURN COALESCE(v_balance, 100);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE OR REPLACE FUNCTION public.get_user_plan(p_user_id UUID)
RETURNS TEXT AS $$
DECLARE v_plan TEXT;
BEGIN
  SELECT plan INTO v_plan FROM public.subscriptions
  WHERE user_id = p_user_id AND status = 'active'
    AND (expires_at IS NULL OR expires_at > NOW())
  ORDER BY expires_at DESC LIMIT 1;
  RETURN COALESCE(v_plan, 'free');
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE OR REPLACE FUNCTION public.get_daily_message_count(p_user_id UUID)
RETURNS INTEGER AS $$
DECLARE v_count INTEGER;
BEGIN
  SELECT message_count INTO v_count FROM public.daily_usage
  WHERE user_id = p_user_id AND usage_date = CURRENT_DATE;
  RETURN COALESCE(v_count, 0);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE OR REPLACE FUNCTION public.increment_daily_usage(p_user_id UUID)
RETURNS INTEGER AS $$
DECLARE v_new_count INTEGER;
BEGIN
  INSERT INTO public.daily_usage (user_id, usage_date, message_count)
  VALUES (p_user_id, CURRENT_DATE, 1)
  ON CONFLICT (user_id, usage_date)
  DO UPDATE SET message_count = public.daily_usage.message_count + 1;
  SELECT message_count INTO v_new_count FROM public.daily_usage
  WHERE user_id = p_user_id AND usage_date = CURRENT_DATE;
  RETURN v_new_count;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE OR REPLACE FUNCTION public.deduct_credits_atomic(
  p_user_id UUID,
  p_amount INTEGER,
  p_description TEXT
) RETURNS TABLE(success BOOLEAN, new_balance INTEGER, error TEXT) AS $$
DECLARE v_current_balance INTEGER;
BEGIN
  SELECT balance INTO v_current_balance FROM public.credits WHERE user_id = p_user_id FOR UPDATE;
  IF NOT FOUND THEN
    RETURN QUERY SELECT FALSE, 0, '积分记录不存在'::TEXT;
    RETURN;
  END IF;
  IF v_current_balance < p_amount THEN
    RETURN QUERY SELECT FALSE, v_current_balance, '积分不足'::TEXT;
    RETURN;
  END IF;
  UPDATE public.credits
  SET balance = balance - p_amount, total_used = total_used + p_amount, updated_at = NOW()
  WHERE user_id = p_user_id;
  INSERT INTO public.credit_transactions (user_id, amount, type, description)
  VALUES (p_user_id, -p_amount, 'deduct', p_description);
  RETURN QUERY SELECT TRUE, v_current_balance - p_amount, NULL::TEXT;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE OR REPLACE FUNCTION public.grant_credits(p_user_id UUID, p_amount INTEGER, p_description TEXT)
RETURNS VOID AS $$
BEGIN
  UPDATE public.credits SET balance = balance + p_amount, updated_at = NOW() WHERE user_id = p_user_id;
  INSERT INTO public.credit_transactions (user_id, amount, type, description)
  VALUES (p_user_id, p_amount, 'grant', p_description);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

